const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    avatar: {
        type: String
    },
    date: {
        type: Date,
        default: Date.now
    }
})

//In mongoDB, user collection will be pluralized as users in model schema, so you will it as 'users' in mongoDB collection.
module.exports = mongoose.model('user', userSchema);